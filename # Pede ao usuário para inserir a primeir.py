# Pede ao usuário para inserir a primeira nota e converte para um número decimal
nota1 = float(input("Informe a primeira nota: "))

# Pede ao usuário para inserir a segunda nota e converte para um número decimal
nota2 = float(input("Informe a segunda nota: "))

# Calcula a média das duas notas
media = (nota1 + nota2) / 2

# Imprime a média calculada
print("A média do aluno é:", media)